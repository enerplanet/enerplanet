import html2canvas from 'html2canvas';
import jsPDF from 'jspdf';
import { Model } from '@/features/model-dashboard/services/modelService';

interface ExportOptions {
  title?: string;
  subtitle?: string;
  includeTimestamp?: boolean;
}

// Format date for display
const formatDate = (date: Date): string => {
  return date.toLocaleDateString('en-GB', {
    day: '2-digit',
    month: 'short',
    year: 'numeric',
    hour: '2-digit',
    minute: '2-digit',
  });
};

// Capture element as image
async function captureElement(element: HTMLElement): Promise<string> {
  const canvas = await html2canvas(element, {
    scale: 2, // Higher quality
    useCORS: true,
    logging: false,
    backgroundColor: '#ffffff',
    windowWidth: element.scrollWidth,
    windowHeight: element.scrollHeight,
  });
  return canvas.toDataURL('image/png');
}

// Export comparison report as PDF
export async function exportComparisonPDF(
  contentRef: HTMLElement,
  model1: Model,
  model2: Model,
  options: ExportOptions = {}
): Promise<void> {
  const {
    title = 'Simulation Comparison Report',
    subtitle,
    includeTimestamp = true,
  } = options;

  // Create PDF (A4 landscape for better chart display)
  const pdf = new jsPDF({
    orientation: 'landscape',
    unit: 'mm',
    format: 'a4',
  });

  const pageWidth = pdf.internal.pageSize.getWidth();
  const pageHeight = pdf.internal.pageSize.getHeight();
  const margin = 15;
  let yOffset = margin;

  // Helper to add text
  const addText = (text: string, x: number, y: number, options: { fontSize?: number; fontStyle?: string; color?: number[] } = {}) => {
    const { fontSize = 12, fontStyle = 'normal', color = [0, 0, 0] } = options;
    pdf.setFontSize(fontSize);
    pdf.setFont('helvetica', fontStyle);
    pdf.setTextColor(color[0], color[1], color[2]);
    pdf.text(text, x, y);
  };

  // Header
  addText(title, margin, yOffset, { fontSize: 20, fontStyle: 'bold' });
  yOffset += 8;

  if (subtitle) {
    addText(subtitle, margin, yOffset, { fontSize: 12, color: [100, 100, 100] });
    yOffset += 6;
  }

  if (includeTimestamp) {
    addText(`Generated: ${formatDate(new Date())}`, margin, yOffset, { fontSize: 10, color: [128, 128, 128] });
    yOffset += 10;
  }

  // Model info section
  addText('Models Compared:', margin, yOffset, { fontSize: 14, fontStyle: 'bold' });
  yOffset += 7;

  // Baseline info
  pdf.setFillColor(59, 130, 246); // Blue
  pdf.rect(margin, yOffset - 3, 3, 12, 'F');
  addText(`Baseline: ${model1.title}`, margin + 6, yOffset, { fontSize: 11, fontStyle: 'bold' });
  yOffset += 5;
  addText(`Region: ${model1.region || 'N/A'} | Period: ${new Date(model1.from_date).toLocaleDateString()} - ${new Date(model1.to_date).toLocaleDateString()}`, margin + 6, yOffset, { fontSize: 9, color: [100, 100, 100] });
  yOffset += 8;

  // Comparison info
  pdf.setFillColor(245, 158, 11); // Amber
  pdf.rect(margin, yOffset - 3, 3, 12, 'F');
  addText(`Comparison: ${model2.title}`, margin + 6, yOffset, { fontSize: 11, fontStyle: 'bold' });
  yOffset += 5;
  addText(`Region: ${model2.region || 'N/A'} | Period: ${new Date(model2.from_date).toLocaleDateString()} - ${new Date(model2.to_date).toLocaleDateString()}`, margin + 6, yOffset, { fontSize: 9, color: [100, 100, 100] });
  yOffset += 12;

  // Capture and add content
  try {
    // Find all chart containers
    const chartContainers = contentRef.querySelectorAll('.bg-card');

    for (let i = 0; i < chartContainers.length; i++) {
      const container = chartContainers[i] as HTMLElement;

      if (yOffset > pageHeight - 80) {
        pdf.addPage();
        yOffset = margin;
      }

      try {
        const imgData = await captureElement(container);

        // Calculate dimensions to fit on page
        const containerAspect = container.offsetWidth / container.offsetHeight;
        const maxWidth = pageWidth - margin * 2;
        const maxHeight = pageHeight - yOffset - margin - 10;

        let imgWidth = maxWidth;
        let imgHeight = imgWidth / containerAspect;

        if (imgHeight > maxHeight) {
          imgHeight = maxHeight;
          imgWidth = imgHeight * containerAspect;
        }

        // Center horizontally
        const xPos = (pageWidth - imgWidth) / 2;

        pdf.addImage(imgData, 'PNG', xPos, yOffset, imgWidth, imgHeight);
        yOffset += imgHeight + 10;
      } catch (err) {
        console.warn(`Failed to capture chart ${i}:`, err);
      }
    }

    // Footer on last page
    pdf.setFontSize(8);
    pdf.setTextColor(128, 128, 128);
    pdf.text('Generated by EnerPlanET - Energy Planning Tool', pageWidth / 2, pageHeight - 5, { align: 'center' });

    // Save PDF
    const filename = `comparison-${model1.title.replaceAll(/\s+/g, '_')}-vs-${model2.title.replaceAll(/\s+/g, '_')}-${new Date().toISOString().split('T')[0]}.pdf`;
    pdf.save(filename);
  } catch (error) {
    console.error('Failed to generate PDF:', error);
    throw new Error('Failed to generate PDF report');
  }
}


